<p>L'Application mobile permet de vous communiquer toute l'information scolaire de votre enfant pour rester informé et vous aider à bien accompagner votre enfant.</p>

<h3>COLLECTE DES DONNÉES PERSONNELLES</h3>
<p>Nous collectons les renseignements suivants :</p>
<ul>
<li>Nom/ Prénom</li>
<li>Adresse électronique</li>
<li>Sexe</li>
<li>Age / Date de naissance</li>
<li>Photo</li>
<li><b>Phone Number</b></li>
</ul>
<p>Ces données personnelles sont recueillies après suite à l'inscription de votre enfant et remplissage de la fiche d'inscription par vos soins ou l'aide de l'agent d'inscription à l'école.</p>
<p>Vos données ne sont en aucun cas transmisses à d'autres parties tierces, nous n’utilisons pas de cookies pour réunir des informations vous concernant.</p>
<h3>FICHIERS JOURNAUX</h3>
<p>Lorsque vous utilisez notre Application mobile, nous collectons et stockons des informations dans les fichiers journaux de nos serveurs. Cela comprend :</p>
Date de votre connexion
Votre adresse IP.
<p>Des données relatives aux événements liés à l’appareil que vous utilisez, tels que plantages, activité du système, paramètres du matériel, type et langue de votre navigateur, date et heure de la requête et URL de provenance.</p>
<h3>DROIT D’OPPOSITION ET DE RETRAIT</h3>
<p>Nous nous engageons à vous offrir un droit d’opposition et de retrait quant à vos renseignements personnels.
<p>Le droit d’opposition s’entend comme étant la possibilité offerte aux internautes de refuser que leurs renseignements personnels soient utilisés à certaines fins mentionnées lors de la collecte.</p>
<p>Le droit de retrait s’entend comme étant la possibilité offerte aux internautes de demander à ce que leurs renseignements personnels ne figurent plus, par exemple, dans une liste de diffusion.</p>
<p>Pour pouvoir exercer ces droits, vous pouvez nous contacter directement l'administration.
<h3>DROIT D’ACCÈS</h3>
<p>Nous nous engageons à reconnaître un droit d’accès et de rectification aux personnes concernées désireuses de consulter, modifier, voire radier les informations les concernant.</p>
<p>Vous pouvez mettre à jour vos informations personnelles via votre application mobile comme vous pouvez le demandé à votre conseiller/agent à l'administration de l'école.</p>
<h3>SÉCURITÉ</h3>
<p>Les renseignements personnels que nous collectons sont conservés dans un environnement sécurisé. Les personnes travaillant pour nous sont tenues de respecter la confidentialité de vos informations. Pour assurer la sécurité de vos renseignements personnels, nous avons recours aux mesures suivantes :</p>

<ul>
<li>Protocole SSL (Secure Sockets Layer)</li>
<li>Gestion des accès – personne autorisée</li>
<li>Gestion des accès – personne concernée</li>
<li>Logiciel de surveillance du réseau</li>
<li>Sauvegarde informatique</li>
<li>Développement de certificat numérique</li>
<li>Identifiant / mot de passe</li>
<li>Pare-feu (Firewalls)</li>
<li>Hébergeur de données de santé agréé HDS ASIP santé</li>
</ul>
<p>Nous nous engageons à maintenir un haut degré de confidentialité en intégrant les dernières innovations technologiques permettant d’assurer la confidentialité de vos données. Toutefois, comme aucun mécanisme n’offre une sécurité maximale, une part de risque est toujours présente lorsque l’on utilise Internet pour transmettre des renseignements personnels.</p>
